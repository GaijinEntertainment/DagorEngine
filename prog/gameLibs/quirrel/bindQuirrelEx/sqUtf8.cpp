eastl::unordered_map<uint32_t, uint32_t> upperToLowerMap;
eastl::unordered_map<uint32_t, uint32_t> lowerToUpperMap;
std::once_flag caseMapsInitFlag;

void init_case_maps()
{
  utf8::iterator<const char *> it_upper(CASE_PAIR_UPPER);
  utf8::iterator<const char *> it_end(CASE_PAIR_UPPER + strlen(CASE_PAIR_UPPER));
  utf8::iterator<const char *> it_lower(CASE_PAIR_LOWER);

  for (; it_upper != it_end; ++it_upper, ++it_lower)
  {
    const uint32_t upper = *it_upper;
    const uint32_t lower = *it_lower;
    upperToLowerMap[upper] = lower;
    lowerToUpperMap[lower] = upper;
  }
}

uint32_t map_to_lower(uint32_t ch)
{
  if (ch < 0x80)
  {
    if (ch >= 'A' && ch <= 'Z')
      return ch + 0x20;
    return ch;
  }

  auto found = upperToLowerMap.find(ch);
  if (found != upperToLowerMap.end())
    return found->second;
  return ch;
}

uint32_t map_to_upper(uint32_t ch)
{
  if (ch < 0x80)
  {
    if (ch >= 'a' && ch <= 'z')
      return ch - 0x20;
    return ch;
  }

  auto found = lowerToUpperMap.find(ch);
  if (found != lowerToUpperMap.end())
    return found->second;
  return ch;
}

template <uint32_t (*MapFn)(uint32_t)>
const char *apply_case_map(const String &srcUtf8, String &resUtf8)
{
  std::call_once(caseMapsInitFlag, init_case_maps);

  clear_and_shrink(resUtf8);

  const int utf8_length = srcUtf8.length();
  const char *utf8_begin = srcUtf8.str();
  const char *utf8_end = utf8_begin + utf8_length;

  utf8::iterator<const char *> it(utf8_begin);
  utf8::iterator<const char *> it_end(utf8_end);

  resUtf8.reserve(utf8_length);

  for (; it != it_end; ++it)
  {
    uint32_t ch = *it;
    ch = MapFn(ch);
    char add[5] = {0, 0, 0, 0, 0}; // max one UTF8 char size is 4 and plus '\0'
    utf8::append(ch, add);
    resUtf8 += add;
  }
  return resUtf8.str();
}

